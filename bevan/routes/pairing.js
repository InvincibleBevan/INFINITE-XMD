const express = require('express');
const fs = require('fs');
const path = require('path');
const pino = require("pino");
const {
    default: makeWASocket,
    useMultiFileAuthState,
    delay,
    makeCacheableSignalKeyStore,
    Browsers
} = require("@whiskeysockets/baileys");

const router = express.Router();

// Utility functions
function generateId() {
    return Math.random().toString(36).substring(2, 15);
}

function removeFile(filePath) {
    if (!fs.existsSync(filePath)) return false;
    fs.rmSync(filePath, { recursive: true, force: true });
    return true;
}

// Format phone number correctly for WhatsApp
function formatPhoneNumber(number) {
    // Remove all non-digits
    let cleanNumber = number.replace(/\D/g, '');
    
    // If number starts with 0, replace with country code (assuming Kenya +254)
    if (cleanNumber.startsWith('0')) {
        cleanNumber = '254' + cleanNumber.substring(1);
    }
    
    // If number doesn't have country code, add +254 (Kenya)
    if (cleanNumber.length === 9) {
        cleanNumber = '254' + cleanNumber;
    }
    
    return cleanNumber;
}

const activeSessions = new Map();

// INSTANT QR Code Generation
router.get('/qr', async (req, res) => {
    const sessionId = generateId();
    const tempDir = path.join(__dirname, 'temp', sessionId);
    fs.mkdirSync(tempDir, { recursive: true });

    console.log('🚀 Starting INSTANT QR generation...');

    try {
        const { state, saveCreds } = await useMultiFileAuthState(tempDir);

        const sock = makeWASocket({
            auth: {
                creds: state.creds,
                keys: makeCacheableSignalKeyStore(state.keys, pino({ level: "fatal" })),
            },
            printQRInTerminal: true,
            logger: pino({ level: "info" }),
            browser: Browsers.macOS("Safari"),
            connectTimeoutMs: 30000,
            keepAliveIntervalMs: 10000,
            // CRITICAL: Add these options for better connection
            markOnlineOnConnect: true,
            syncFullHistory: false,
            transactionOpts: {
                maxCommitRetries: 3,
                delayBetweenTriesMs: 1000
            }
        });

        // Store the socket IMMEDIATELY
        activeSessions.set(sessionId, { sock, tempDir, createdAt: Date.now() });

        let qrSent = false;

        // Send QR code INSTANTLY
        sock.ev.on("connection.update", async (update) => {
            const { connection, lastDisconnect, qr } = update;

            if (qr && !qrSent) {
                console.log('📱 QR code generated INSTANTLY');
                qrSent = true;
                
                res.json({ 
                    success: true, 
                    qr: qr,
                    sessionId: sessionId 
                });
            }

            if (connection === "open") {
                console.log('✅ WhatsApp CONNECTED successfully');
                
                try {
                    const credsPath = path.join(tempDir, 'creds.json');
                    if (fs.existsSync(credsPath)) {
                        const sessionData = fs.readFileSync(credsPath, 'utf8');
                        const base64Session = Buffer.from(sessionData).toString('base64');
                        const infinitySession = "INFINITY_" + base64Session;

                        const message = `🔐 *YOUR WHATSAPP SESSION ID* - INFINITE-XMD

*Session ID:*
\`\`\`
${infinitySession}
\`\`\`

*Generated:* ${new Date().toLocaleString()}

💻 Generated by Bevan Society`;

                        await sock.sendMessage(sock.user.id, { text: message });
                        console.log('✅ Session sent to user');
                    }
                } catch (error) {
                    console.error('Error sending session:', error);
                }

                // Cleanup after success
                setTimeout(() => {
                    sock.ws.close();
                    removeFile(tempDir);
                    activeSessions.delete(sessionId);
                }, 5000);
            }
            
            else if (connection === "close") {
                console.log('❌ Connection closed');
                removeFile(tempDir);
                activeSessions.delete(sessionId);
            }
        });

        // Timeout after 30 seconds
        setTimeout(() => {
            if (activeSessions.has(sessionId) && !qrSent) {
                sock.ws.close();
                removeFile(tempDir);
                activeSessions.delete(sessionId);
                if (!res.headersSent) {
                    res.status(408).json({ error: 'QR generation timeout' });
                }
            }
        }, 30000);

    } catch (error) {
        console.error('❌ QR generation error:', error);
        removeFile(tempDir);
        if (!res.headersSent) {
            res.status(500).json({ 
                error: 'Failed to generate QR code',
                details: error.message 
            });
        }
    }
});

